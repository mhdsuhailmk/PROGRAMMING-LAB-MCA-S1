def area():
    l1=int(input("Enter the length of cuboid:\t"))
    w=int(input("Enter the width of cuboid:\t"))
    h=int(input("Enter the height of cuboid:\t"))
    print("Area of cuboid is:\t",2*((l1*w)+(l1*h)+(w*h)))
def perimeter():
    l1=int(input("Enter the length of cuboid:\t"))
    w=int(input("Enter the width of cuboid:\t"))
    h=int(input("Enter the height of cuboid:\t"))
    print("Perimeter of cuboid is:\t",4*(l1+w+h))